class CalculatorClass {
     public CalculatorClass() {
    }
    
    public int add( int num1 , int num2){
        int ad ;
        ad = num1 + num2;
        return ad;         
    }
    public int sub( int num1 , int num2){
        int ad;
        ad = num1 - num2;
        return ad;         
    }
    public int mult( int num1 , int num2){
        int ad;
        ad = num1 * num2;
        return ad;         
    }
    public int div( int num1 , int num2){
        int ad;
        ad = num1 / num2;
        return ad;         
    }
}
